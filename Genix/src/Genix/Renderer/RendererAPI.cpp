#include "gxpch.h"
#include "Genix/Renderer/RendererAPI.h"
#include <glad/glad.h>

RendererAPI::API RendererAPI::s_API = API::OpenGL;

Scope<RendererAPI> RendererAPI::Create()
{
	return CreateScope<RendererAPI>();
}

static GLenum OpenGLBlendFunc(BlendFuncArgument arg)
{
	switch (arg)
	{
	case BlendFuncArgument::OneMinusSrcAlpha:	return GL_ONE_MINUS_SRC_ALPHA;
	case BlendFuncArgument::SrcAlpha:			return GL_SRC_ALPHA;
		
	}
	ASSERT_CORE(false, "Unknown arg!");
	return 0;
}

void OpenGLMessageCallback(
	unsigned source,
	unsigned type,
	unsigned id,
	unsigned severity,
	int length,
	const char* message,
	const void* userParam)
{
	switch (severity)
	{
	case GL_DEBUG_SEVERITY_HIGH:         LOG_CORE_CRITICAL(message); return;
	case GL_DEBUG_SEVERITY_MEDIUM:       LOG_CORE_ERROR(message); return;
	case GL_DEBUG_SEVERITY_LOW:          LOG_CORE_WARN(message); return;
	case GL_DEBUG_SEVERITY_NOTIFICATION: LOG_CORE_TRACE(message); return;
	}

	ASSERT_CORE(false, "Unknown severity level!");
}

void RendererAPI::Init()
{
#ifdef GX_DEBUG
	glEnable(GL_DEBUG_OUTPUT);
	glEnable(GL_DEBUG_OUTPUT_SYNCHRONOUS);
	glDebugMessageCallback(OpenGLMessageCallback, nullptr);

	glDebugMessageControl(GL_DONT_CARE, GL_DONT_CARE, GL_DEBUG_SEVERITY_NOTIFICATION, 0, NULL, GL_FALSE);
#endif
	
	glEnable(GL_DEPTH_TEST);
	// set depth function to less than AND equal for skybox depth trick.
	glDepthFunc(GL_LEQUAL);
	// enable seamless cubemap sampling for lower mip levels in the pre-filter map.
	glEnable(GL_TEXTURE_CUBE_MAP_SEAMLESS);
	
	glEnable(GL_BLEND);
	glBlendFunc(GL_SRC_ALPHA, GL_ONE_MINUS_SRC_ALPHA);
}

void RendererAPI::SetViewport(uint32 x, uint32 y, uint32 width, uint32 height)
{
	glViewport(x, y, width, height);
}

void RendererAPI::SetClearColor(const glm::vec4& color)
{
	glClearColor(color.r, color.g, color.b, color.a);
}

void RendererAPI::SetActiveTexture(const uint8 slot)
{
	glActiveTexture(GL_TEXTURE0 + slot);
}

void RendererAPI::Clear()
{
	glClear(GL_COLOR_BUFFER_BIT | GL_DEPTH_BUFFER_BIT);
}

void RendererAPI::ResetFrameBuffer()
{
	glBindFramebuffer(GL_FRAMEBUFFER, 0);
}

void RendererAPI::SetBlendFunc(const BlendFuncArgument arg1, const BlendFuncArgument arg2)
{
	glBlendFunc(OpenGLBlendFunc(arg1), OpenGLBlendFunc(arg2));
}

void RendererAPI::DrawIndexed(const Ref<VertexArray>& vertexArray)
{
	glDrawElements(GL_TRIANGLES, vertexArray->GetIndexBuffer()->GetCount(), GL_UNSIGNED_INT, nullptr);
}

